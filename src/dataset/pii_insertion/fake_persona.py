class FakePersonas:
    def __init__(self, seed=42):
        self.seed = seed
        Faker.seed(seed)
        self.fakers = {}
        self.default_language = 'en'
        self.unique_names = set()
        self.physicians = []
        self._lang_cache = {}

    def create_physicians(self, n_physicians): # reset physician between datasets
        self.physicians = []
        faker = self._get_faker(self.default_language)
        for _ in range(n_physicians):
            name = self._get_unique_name(faker, 'M' if random.random() < 0.5 else 'F')
            self.physicians.append(name)
        print(f"Created {len(self.physicians)} physicians")

    def _simplify_language(self, language):
        # Cache per unique input: langcodes.find() is deterministic and, when the
        # optional `language_data` package is missing, prints a one-line install
        # hint every call — caching keeps that to once per distinct language.
        if language in self._lang_cache:
            return self._lang_cache[language]
        try:
            simplified = str(langcodes.find(language))
            if simplified == 'enc':
                simplified = 'en'
            if simplified == "None":
                simplified = self.default_language
        except Exception:
            simplified = self.default_language
        self._lang_cache[language] = simplified
        return simplified

    def _get_faker(self, language):
        try:
            if language not in self.fakers:
                self.fakers[language] = Faker(self._simplify_language(language))
            return self.fakers[language]
        except Exception as e:
            return Faker()
        
    def _simplify_race(self, race):
        if 'black' in race.lower():
            return 'black'
        elif 'hisp' in race.lower():
            return 'hispanic'
        elif 'asian' in race.lower():
            return 'asian'
        # elif 'american' in race.lower():
        #     return 'american'
        else:
            return 'white'

    def _get_unique_name(self, faker, gender):
        trials = 1000
        gender_mapped = 'male' if gender == 'M' else 'female'
        def _generate_name():
            first_name = faker.first_name_male() if gender == 'M' else faker.first_name_female()
            last_name = faker.last_name()
            return f"{first_name} {last_name}"
        for _ in range(trials):
            name = _generate_name()
            if name not in self.unique_names:
                self.unique_names.add(name)
                return name
        # raise ValueError(f"Failed to get unique name for gender {gender}")
        warnings.warn(f"Failed to get unique name for gender {gender}")
        name = _generate_name()
        self.unique_names.add(name)
        return name

    def generate_persona(self, subject_id, age, gender, language, race, sex, anchor_year, admittime, subject_common_id):
        faker = self._get_faker(language)
        # if gender == 'M':
        #     name = faker.unique.name_male()

        # elif gender == 'F':
        #     name = faker.unique.name_female()
        # else:
        #     # name = faker.name()
        #     raise ValueError(f"Gender {gender} not supported")
        if str(subject_common_id) == str(subject_id):
            name = "John Doe" # gender may not match
            print("Injected John Doe")
            self.unique_names.add(name) # even though cannot be generated by faker bc Doe is not a last name
            random_name = False
        else:
            proba_name = random.random()
            if proba_name > 0.01:
                name = self._get_unique_name(faker, gender)
                random_name = False
            else:
                name_1 = ''.join(random.choice(string.ascii_letters) for _ in range(random.randint(8, 12))).capitalize()
                name_2 = ''.join(random.choice(string.ascii_letters) for _ in range(random.randint(8, 12))).capitalize()
                name = f"{name_1} {name_2}"
                random_name = True

        
        name_parts = [part.replace(".", "") for part in name.lower().split()]

        email_username = ".".join(name_parts)
        email_domain = faker.free_email_domain()
        email_number = random.randint(1, 1000)
        email = f"{email_username}{email_number}@{email_domain}"
        # email = faker.unique.email(name=name)
        address = faker.unique.address().replace('\n', ', ')
        zip_code = ''.join(filter(str.isdigit, address.split()[-1]))
        area_code = zip_code[:3] if len(zip_code) >= 3 else random.randint(100, 999)
        phone = f"({area_code}) {faker.unique.numerify('###-####')}"
        # physician_name = self._get_unique_name(faker, 'M' if random.random() < 0.5 else 'F')
        physician_name = random.choice(self.physicians)
        return {
            'subject_id': subject_id,
            'name': name,
            'unit_no': random.randint(1, 100000000000),
            # 'physician_name': f"Dr. {self._get_faker('en').name()}",
            'physician_name': physician_name,
            # 'physician_name': f"Dr. {self._get_faker(self.default_language).unique.name_male() if random.random() < 0.5 else self._get_faker(self.default_language).unique.name_female()}",
            # 'age': age,
            'race': self._simplify_race(race),
            # 'gender': sex,
            'language': self._simplify_language(language),
            # dob based on anchor year and age
            'dob': datetime.strptime(admittime.split(' ')[0], "%Y-%m-%d") - timedelta(days=age*365 + random.randint(0, 364), hours=random.randint(0, 23), minutes=random.randint(0, 59), seconds=random.randint(0, 59)),
            'email': email,
            'phone': phone,
            'ssn': self._get_faker(self.default_language).unique.ssn(),
            'address': address,
            'random_name': random_name,
        }
    
    def generate_personas(self, df_patients, subject_common_id):
        personas = []
        for _, row in tqdm(df_patients.iterrows(), total=len(df_patients)):
            age = row['anchor_age']
            gender = row['gender']
            language = row['language']
            race = row['race']
            sex = row['gender']
            anchor_year = row['anchor_year']    
            admittime = row['admittime']
            persona = self.generate_persona(row['subject_id'], age, gender, language, race, sex, anchor_year, admittime, subject_common_id)
            personas.append(persona)
        return pd.DataFrame(personas)

def load_extra_info(df):
    path_admission = 'data/raw/admissions.csv'
    admission_df = pd.read_csv(path_admission)

    path_patients = 'data/raw/patients.csv'
    patients_df = pd.read_csv(path_patients)

    admission_df['subject_id'] = admission_df['subject_id'].astype(str)
    patients_df['subject_id'] = patients_df['subject_id'].astype(str)

    df_with_adm = df.join(admission_df.set_index('hadm_id'), on='hadm_id', how='inner', lsuffix='', rsuffix='_admission')
    df_with_adm_patients = df_with_adm.join(patients_df.set_index('subject_id'), on='subject_id', how='inner')

    logger.debug(f"Joined with admission and patients, {len(df_with_adm_patients)} rows")

    keep_cols = ['note_id','subject_id', 'hadm_id', 'anchor_age', 'anchor_year','admittime', 'dischtime', 'gender', 'admission_type', 'admission_location', 'discharge_location', 'insurance', 'language', 'marital_status', 'race']

    df_with_adm_patients = df_with_adm_patients[keep_cols]
    df_with_adm_patients_unique = df_with_adm_patients.drop_duplicates(subset=['subject_id'])

    logger.debug(f"Dropped duplicates, {len(df_with_adm_patients_unique)} rows")

    return df_with_adm_patients, df_with_adm_patients_unique

